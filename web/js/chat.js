//Show Friendslist
$(function () {
    $('#fancyShow').on('click', function () {
        $('#friends').show();
        $('#friends').show();
        $('#friends').show();
    });
});

//Hide Friendslist
$(function () {
    $('#fancyHide').on('click', function () {
        $('#friends').hide();
        $('#friends').hide();
        $('#friends').hide();
    });
});